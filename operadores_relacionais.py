# Operadores de comparação (relacionais)
# OP     Significado Exemplo (True)
# >    Maior           2 > 1
# >=   maior ou igual  2 >= 2
# <    menor           1 < 2
# <=   menor ou igual  2 <= 2
# ==   igual           'a' == 'a'
# !=   diferebte       "a" != 'b'

maior = 2 > 7
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 3 <= 4
igual = 'a' == 'z'
diferente = 'b' != 'c'

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)